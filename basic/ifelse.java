
public class ifelse {

    public static void main(String[] args) {
        int marks = 90;
        if (marks < 50) {
            System.out.println(marks+"% 1st class");
        } else if (marks < 80) {
            System.out.println(marks+"% Second Class");
        } else {
            System.out.println(marks+"% Outstanding");
        }
    }
}
